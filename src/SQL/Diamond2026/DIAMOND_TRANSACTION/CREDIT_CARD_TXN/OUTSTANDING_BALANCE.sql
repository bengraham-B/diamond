SET @ACCOUNT_ID = '32acc5e7-14c7-11f1-a3e0-ce6cdc3544e3'; # DEV_ACCOUNT

SELECT
    SUM(
        CASE WHEN TXN_TYPE='INCREASE' THEN AMOUNT ELSE -AMOUNT END
    ) AS OUTSTANDING_BALANCE
FROM
    DIAMOND_TRANSACTION
WHERE
    ACCOUNT_ID=@ACCOUNT_ID AND
    SOURCE='CREDIT_CARD'