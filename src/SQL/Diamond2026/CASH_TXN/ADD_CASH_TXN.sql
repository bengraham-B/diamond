SET @ACCOUNT_ID = 'c5201fe4-02a2-11f1-8f82-0242ac160002'; -- DEV_ACCOUNT_001

-- INSERT CASH_TXN =====================================================
INSERT INTO CASH_TXN
    (CASH_TXN_ID, TRANSACTION_SOURCE, DATE, DETAILS, AMOUNT, ACCOUNT_ID)
