    (@CASH_TXN_ID, @SOURCE_TYPE, @TXN_DATE, @TXN_DESC, @TXN_AMOUNT,  @ACCOUNT_ID);

-- INSERT GL_HEADER =====================================================
INSERT INTO GL_HEADER
    (GL_HEADER_ID, SOURCE_TYPE, TRANSACTION_ID, details, ACCOUNT_ID)
