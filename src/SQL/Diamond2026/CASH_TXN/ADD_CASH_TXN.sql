    (UUID(), (SELECT GL_ACCOUNT_ID FROM GL_ACCOUNT WHERE GL_ACCOUNT_CODE = @DEBIT_GL_CODE AND ACCOUNT_ID=@ACCOUNT_ID), @GL_HEADER_ID, 'DEBIT',  @TXN_AMOUNT, @ACCOUNT_ID),
