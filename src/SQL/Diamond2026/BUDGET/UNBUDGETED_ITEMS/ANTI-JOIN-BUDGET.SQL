SELECT
    GL_ACCOUNT_NAME,
    SUM(AMOUNT)
FROM
    DIAMOND_TRANSACTION

LEFT JOIN GL_ACCOUNT ON DIAMOND_TRANSACTION.GL_ACCOUNT_ID = GL_ACCOUNT.GL_ACCOUNT_ID
LEFT JOIN BUDGET ON DIAMOND_TRANSACTION.GL_ACCOUNT_ID = BUDGET.GL_ACCOUNT_ID WHERE BUDGET.GL_ACCOUNT_ID IS NULL

GROUP BY
    GL_ACCOUNT_NAME
